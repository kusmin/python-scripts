nome = "Pycodebr"
hobby = "programar"

# Concatenação simples usando +
s = "Meu nome é " + nome + " e meu hobby é " + hobby
print(s)

# Concatenando usando Fstring
s = f"Meu nome é {nome} e meu hobby é {hobby}"
print(s)

# Concatenação usando format
s = "Meu nome é {} e meu hobby é {}".format(nome, hobby)
print(s)
